const calculator = {
  add: function (a, b) {
    console.log(a + b);
  },
  minus: function (a, b) {
    console.log(a - b);
  },
  divide: function (a, b) {
    console.log(a / b);
  },
  multi: function (a, b) {
    console.log(a * b);
  },
  power: function (a, b) {
    console.log(a ** b);
  },
};
calculator.add(1, 1);
calculator.minus(45, 5);
calculator.divide(54, 9);
calculator.multi(3, 12);
calculator.power(4, 4);
